package com.yezp;

public class Main {

    public static void main(String[] args) {
        int x = 10;
        int y = 30;

        int add = x + y;
        int sub = add - y;
        int mutil = add * sub;
        int divide = mutil / sub;

        if (divide > 0) {
            for ( int i = 0; i < divide; i++) {
                i++;
            }
        }
    }

}
